# noinspection PyDocstring
def test_address_to_bin(a, ra, rm):
    # noinspection PyDocstring
    def test(self):
        net = ipaddr.IPNetwork(a)
        addr = net.ip
        mask = net.netmask

        self.assertEqual(ra, uipcalc.address_to_bin(addr))
        self.assertEqual(rm, uipcalc.address_to_bin(mask))

    return test


class TestUIPCALC(TestCase):
    pass

